package com.sun.xml.ws.addressing.model;

/**
 * @author Arun Gupta
 */
public class Relationship {
    private String id;
    private String type;

    public Relationship() {
    }

    public Relationship(String id, String type) {
        this.id = id;
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
